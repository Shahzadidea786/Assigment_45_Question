"use strict";
//Name Cases: Store a person’s name in a variable, and then print that person’s name in lowercase, uppercase, and titlecase.
let name2 = "Muhammad Ali";
// lowercase 
let lowercase = name2.toLocaleLowerCase();
// uppercase
let upercase = name2.toUpperCase();
// titlecase
let split = name2.split(' ');
console.log(lowercase);
console.log(upercase);
console.log(split);
